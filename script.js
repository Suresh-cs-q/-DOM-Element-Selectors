// Get elements by tag name
var a = document.getElementsByTagName('li');
console.log(a);

// Get elements by class name
var b = document.getElementsByClassName('hero');
console.log(b);

// Get element by ID
var c = document.getElementById('hero');
console.log(c);

// Query selector: Select one element by ID
var d = document.querySelector('#hero');
console.log(d);

// Query selector: Select one element by class
var d = document.querySelector('.hero');
console.log(d);

// Query selector all: Select all elements by class
var e = document.querySelectorAll('.hero');
console.log(e);